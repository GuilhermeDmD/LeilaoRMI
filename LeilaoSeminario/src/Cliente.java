public class Cliente {
    private String nome;
    private int lance;

    public Cliente(String nome, int lance) {
        this.nome = nome;
        this.lance = lance;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getLance() {
        return lance;
    }
    public void setLance(int lance) {
        this.lance = lance;
    }
    
   
    

}
